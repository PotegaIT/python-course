def kwadrat(x): 
    return x ** 2  # Zwraca kwadrat liczby / Returns the square of the number

def srednia(lista): 
    return sum(lista) / len(lista)  # Zwraca średnią z listy liczb / Returns the average of a list of numbers

def czy_parzysta(x): 
    return x % 2 == 0  # Zwraca True, jeśli liczba jest parzysta / Returns True if the number is even
